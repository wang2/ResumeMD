function buildHeaderHtml(header) {
  const { name, title, contactInfo } = header;
  let html = `<h1>${name}</h1>\n<div class="title">${title}</div>\n<div class="contact">\n`;

  contactInfo.forEach((info, idx) => {
    html += `  <div class="contact-item">\n    ${ICONS[info.type] || ''}\n    ${info.value}\n  </div>\n`;
    if (idx < contactInfo.length - 1) {
      html += `  <span class="separator">|</span>\n`;
    }
  });

  html += `</div>\n`;
  return html;
}

function buildFullHtml(headerHtml, bodyHtml, css) {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<style>
${css}
</style>
</head>
<body>
${headerHtml}
${bodyHtml}
</body>
</html>`;
}

module.exports = { buildHeaderHtml, buildFullHtml };
